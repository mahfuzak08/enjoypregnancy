										<p>Almost a year since the emergence of Covid-19, science has revealed new findings about its symptoms, transmission and treatment. Here’s the latest</p>
										<h3>Quick facts</h3>
										<ul>
											<li>Men are more likely to suffer severe symptoms of Covid-19</li>
											<li>Women may be more likely to suffer ‘Long COVID-19’, though more research is needed to confirm this</li>
											<li>An increase in rapid swab testing could help stop the spread</li>
											<li>Sneezing, which is common with colds, does not seem to be a symptom of Covid-19</li>
										</ul>
										<p>It’s less than a year since Covid-19 it spreads around a world. And until recently, we knew very little about the new coronavirus — other than its potentially life-threatening nature and origin from Wuhan.</p>
										<p>Now, while there are still many unanswered questions, we know a lot more.</p>

										<h3>How Covid-19 spreads</h3>

										<p>Initially, health advice focused on hand-washing and decontaminating surfaces to help stop the spread. One benefit of this improved hygiene was a sudden drop in vomiting bugs like norovirus.</p>
										<p>But now we know Covid-19 is spread primarily through respiratory transmission. This means breathing in microscopic particles of virus which have been exhaled by someone infected with Covid-19 — which is why masks reduce transmission.</p>
										<p>Coughing is the most obvious way these viral droplets are spread. But heavy breathing, talking and singing all release plumes of respiratory particles which can spread Covid-19.</p>
										<p>Early in the infection, viral load in the upper respiratory tract like the throat is very high, so it’s easy for someone to spread Covid-19 without knowing it. Researchers at Oxford University found that 41% of transmission occurs before the onset of symptoms, and 35% on the day they appear. That’s why tracing and isolation are so important.</p>

										<h3>New Covid-19 symptoms</h3>
										<p>We now know Covid-19 presents in many ways, and symptoms include:</p>
										<ul>
											<li>Fever or chills</li>
											<li>Cough</li>
											<li>Shortness of breath or difficulty breathing</li>
											<li>Fatigue</li>
											<li>Muscle or body aches</li>
											<li>Headache</li>
											<li>New loss of taste or smell</li>
											<li>Sore throat</li>
											<li>Congestion or runny nose</li>
											<li>Nausea or vomiting</li>
											<li>Diarrhoea</li>
											<li>Rash resembling hives, prickly heat or chilblains/blue toe</li>
										</ul>
										<p>
											Winter brings an increase in seasonal flu and common colds. This means that without testing, it will be difficult to differentiate between these infections and a mild case of Covid-19. Sneezing, which is common with colds, does not seem to be a common symptom of Covid-19.
										</p>
										<p>
											A rash, or reddish and purplish bumps on the fingers or toes (sometimes called ‘Covid fingers and toes) are also being seen as symptoms of coronavirus. ‘Rash is sometimes seen with other auto-immune conditions and could be a sign of damage to blood vessels or just the effect of a general “immune-storm” in the body.
										</p>

										<h3>Those at increased risk</h3>
										<p>From the outset, it was clear that older people are at high risk. We now know that people who are overweight or obese, or of black or Asian (BAME) descent are also more likely to catch Covid-19 and become seriously ill.</p>
										<p>In part, the higher risk in BAME people is linked to socio-economic factors like deprivation, several generations living under one roof, and having jobs which can’t be done from home.</p>
										<p>But genetics appears to play a part, too. An inherited gene that’s carried by around 50% of people in South Asia can increase the risk of respiratory failure.</p>

										<h3>Long Covid</h3>
										<p>While most people make a full recovery, some develop a collection of longer-lasting, persistent symptoms that include chronic fatigue, recurrent fever and cognitive deficits sometimes described as ‘brain fog’.</p>
										<p>In fact, Long Covid is now the term used for symptoms which persist for 3 months or more. It often occurs in people who were previously fit and healthy and had only mild symptoms initially. This is not simply a respiratory disease, it’s a systemic disease which gets into the body, including sometimes the central nervous system, via a respiratory tract infection.</p>

										<h3>Gender differences</h3>
										<p>Reports coming out of China that more men than women were dying, were at first put down to the fact that men were also more likely to smoke.</p>
										<p>But the same trend was seen elsewhere. We now know that while men and women have the same chance of catching the coronavirus, men are more likely to suffer severe symptoms and twice as likely to die.</p>
										<p>However, women have twice the risk of developing Long Covid. This may be due to gender differences in immune function relating to women’s child-bearing role.</p>

										<h3>Better treatments</h3>
										<p>We now know the malaria drug chloroquine does more harm than good.</p>
										<p>But some steroid medication may improve survival (though scientists don’t know for certain how this works, it’s probably by heading off cytokine storm – a hyper-inflammatory immune response associated with Covid-19).</p>
										<p>Doctors are also getting better at identifying which Covid patients are most at risk of developing blood clots, and preventing problems by treating them with blood-thinning drugs.</p>
										<p>It’s also been shown that remdesivir — an antiviral drug originally developed to treat Ebola — has been shown to speed recovery.</p>

										<h3>Vaccination:</h3>
										<p>At last, some good news about Covid-19! After many months of intense research by the entire scientific community there is now a licensed Covid-19 vaccine – and more than 100 others in development. In the UK, a vaccine developed by the US pharmaceutical company Pfizer and German company BioNTech was licensed for use on 2nd December 2020.</p>
